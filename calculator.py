# Below is a code of a simple calculator module in Python

def add(a, b):
    """Return the sum of two numbers."""
    return a + b
def subtract(a, b):
    """Return the difference of two numbers."""
    return a - b

def multiply(a, b):
    """Return the product of two numbers."""
    return a * b

def divide(a, b):
    """Return the quotient of two numbers."""
    if b != 0:
        return a / b
    return None
def power(a, b):
    """Return a raised to the power of b."""
    return a ** b

